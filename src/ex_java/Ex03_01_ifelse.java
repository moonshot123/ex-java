package ex_java;

public class Ex03_01_ifelse {

	public static void main(String[] args) {

		int num1=10, num2=20;
		
		if(num1>num2){
			System.out.println("num1이 더 크다.");
			System.out.println("호호호");
			
		}else if(num1<num2){
			System.out.println("num2이 더 크다.");
			System.out.println("우하하");
		}else{
			System.out.println("num2이 같다.");
			System.out.println("히히히");
		}
		
		System.out.println("if~else 공부하는 중");
		
	}

}
